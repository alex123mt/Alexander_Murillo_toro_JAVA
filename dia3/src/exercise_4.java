public class exercise_4 {
    public static void main(String[] args) {
        // figura de rombo con *

        for (int i = 0; i < 10; i++) {
            System.out.println(" ");
            for (int j = 0; j < 10 + i; j++) {
                System.out.print("* ");
            }
            
            
        }
    }
}
